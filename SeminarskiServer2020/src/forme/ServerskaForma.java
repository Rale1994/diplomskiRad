    private void pokreniServer() {
        ps = new PokretanjeServera();
        ps.start();
        lblPokretanjeServera.setText("Server je pokrenut!");
        aktivan = true;

    }

    private void zaustaviServer() {
        if (aktivan == false) {
            JOptionPane.showMessageDialog(this, "Server mora biti pokrenut");
            return;
        } else {
            int odgovor = JOptionPane.showConfirmDialog(this, "Da li zelite da zuastavite server?", "Potvrda", JOptionPane.YES_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (odgovor == JOptionPane.YES_OPTION) {
                ps.zaustaviNit();
                lblZaustavi.setText("Server je zaustavljen!");
                lblPokretanjeServera.setVisible(false);
                aktivan=false;
            } else {

                return;
            }
        }

    }

