    private void postaviPodatke() {
        txtIme.setText("" + klijent.getIme());
        txtPrezime.setText("" + klijent.getPrezime());
        txtJMBG.setText("" + klijent.getJmbg());
        txtKontaktTelefon.setText("" + klijent.getKontaktTelefon());
        txtUlica.setText("" + klijent.getUlica());
        txtBroj.setText("" + klijent.getBroj());
    }

    private void comboAdvokat() throws Exception {
        ArrayList<Advokat> listaAdvokata = KontrolerKlijent.getInstanca().sviAdvokati();
        comboAdvokat.removeAllItems();
        for (Advokat advokat : listaAdvokata) {
            comboAdvokat.addItem(advokat);
        }
    }

    private void comboPrebivaliste() throws Exception {
        ArrayList<Prebivaliste> listaPrebivalista = KontrolerKlijent.getInstanca().svaPrebivalista();
        comboPrebivaliste.removeAllItems();
        for (Prebivaliste prebivaliste : listaPrebivalista) {
            comboPrebivaliste.addItem(prebivaliste);
        }
    }

